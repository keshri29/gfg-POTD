class Solution {
  public:
    string armstrongNumber(int n){
        // code here
        int num=n;
        int ans=0,d=0;
        while(n>0){
            d=n%10;
            ans=ans+(d*d*d);
            n=n/10;
        }
        if(num==ans){
            return "Yes";
        }
        else{
            return "No";
        }
    }
};

